# incremental assigned operators
x = int(input("Enter a number: "))
y = int(input("Enter a second number: "))

c = y
print(f"{c = }")
c += x
print(f"{c = }")
c -= y
print(f"{c = }")
c *= x
print(f"{c = }")
c /= y
print(f"{c = }")
c %= x
print(f"{c = }")
c //= y
print(f"{c = }")
c **= x
print(f"{c = }")